namespace Lapis.Level
{
	/// <summary>
	/// ID numbers for vanilla Minecraft dimensions
	/// </summary>
	public enum Dimension : int
	{
		/// <summary>
		/// Normal (overworld) dimension
		/// </summary>
		Normal = 0,

		/// <summary>
		/// Hell-ish realm with lava everywhere
		/// </summary>
		Nether = -1,

		/// <summary>
		/// The End - home of the Endermen and Enderdragon
		/// </summary>
		End = 1
	}
}
